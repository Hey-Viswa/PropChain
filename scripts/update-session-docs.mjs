import { execFileSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";

const repoRoot = process.cwd();
const docsDir = path.join(repoRoot, "docs");
const outputPath = path.join(docsDir, "WORK_HISTORY.md");
const handoverPath = path.join(docsDir, "MAIN_HANDOVER.md");
const masterChangelogPath = path.join(docsDir, "MASTER_CHANGELOG.md");
const protectedPlanPath = path.join(docsDir, "PropChain_System_Plan.md");

function runGit(args) {
  return execFileSync("git", args, {
    cwd: repoRoot,
    encoding: "utf8",
  }).trim();
}

function runGitRaw(args) {
  return execFileSync("git", args, {
    cwd: repoRoot,
    encoding: "utf8",
  });
}

function toUtc(isoOrOffsetDate) {
  const date = new Date(isoOrOffsetDate);
  if (Number.isNaN(date.getTime())) {
    return isoOrOffsetDate;
  }
  return date.toISOString();
}

function escapeCell(value) {
  return value.replace(/\|/g, "\\|");
}

function toPosix(relativePath) {
  return relativePath.split(path.sep).join("/");
}

function listDocsFiles(rootDir) {
  const collected = [];

  function walk(currentDir) {
    const entries = fs.readdirSync(currentDir, { withFileTypes: true });
    for (const entry of entries) {
      const absolutePath = path.join(currentDir, entry.name);
      if (entry.isDirectory()) {
        walk(absolutePath);
        continue;
      }
      const relativePath = toPosix(path.relative(repoRoot, absolutePath));
      collected.push(relativePath);
    }
  }

  walk(rootDir);
  return collected.sort((a, b) => a.localeCompare(b));
}

function getCommitFileCount(fullHash) {
  const output = runGitRaw(["show", "--pretty=format:", "--name-only", fullHash]);
  const files = output
    .split(/\r?\n/g)
    .map(line => line.trim())
    .filter(Boolean);
  return new Set(files).size;
}

function getCommits() {
  const raw = runGitRaw([
    "log",
    "--reverse",
    "--pretty=format:%H%x1f%h%x1f%aI%x1f%an%x1f%s%x1e",
  ]);

  const records = raw
    .split("\x1e")
    .map(record => record.trim())
    .filter(Boolean);

  return records.map(record => {
    const [fullHash, shortHash, authoredAt, author, subject] = record.split("\x1f");
    return {
      fullHash,
      shortHash,
      authoredAt,
      authoredAtUtc: toUtc(authoredAt),
      author,
      subject,
      filesChanged: getCommitFileCount(fullHash),
    };
  });
}

function extractHeadings(filePath, headingRegex) {
  if (!fs.existsSync(filePath)) {
    return [];
  }
  const content = fs.readFileSync(filePath, "utf8");
  return Array.from(content.matchAll(headingRegex)).map(match => match[1].trim());
}

function readFileIfExists(filePath) {
  if (!fs.existsSync(filePath)) {
    return "";
  }
  return fs.readFileSync(filePath, "utf8");
}

function extractSection(content, sectionHeading) {
  if (!content) {
    return "";
  }

  const headingPattern = new RegExp(`^##\\s+${sectionHeading}\\s*$`, "m");
  const startMatch = content.match(headingPattern);
  if (!startMatch || startMatch.index === undefined) {
    return "";
  }

  const startIndex = startMatch.index;
  const rest = content.slice(startIndex);
  const nextHeadingMatch = rest.slice(startMatch[0].length).match(/^##\s+/m);

  if (!nextHeadingMatch || nextHeadingMatch.index === undefined) {
    return rest.trim();
  }

  const endIndex = startIndex + startMatch[0].length + nextHeadingMatch.index;
  return content.slice(startIndex, endIndex).trim();
}

function extractMasterEntries(content) {
  if (!content) {
    return "";
  }

  const firstEntry = content.match(/^##\s+\d{4}-\d{2}-\d{2}T[^\n]*$/m);
  if (!firstEntry || firstEntry.index === undefined) {
    return "";
  }

  return content.slice(firstEntry.index).trim();
}

function buildMarkdown() {
  const branch = runGit(["rev-parse", "--abbrev-ref", "HEAD"]);
  const totalCommits = Number.parseInt(runGit(["rev-list", "--count", "HEAD"]), 10);
  const commits = getCommits();
  const firstCommit = commits[0];
  const latestCommit = commits[commits.length - 1];

  const docsFiles = listDocsFiles(docsDir);

  const handoverContent = readFileIfExists(handoverPath);
  const masterContent = readFileIfExists(masterChangelogPath);
  const sessionLogMirror = extractSection(handoverContent, "Session Log");
  const masterEntriesMirror = extractMasterEntries(masterContent);

  const handoverEntries = extractHeadings(
    handoverPath,
    /^###\s+(\d{4}-\d{2}-\d{2}T[^\n]+)$/gm
  );
  const masterEntries = extractHeadings(
    masterChangelogPath,
    /^##\s+(\d{4}-\d{2}-\d{2}T[^\n]+)$/gm
  );

  const generatedAt = new Date().toISOString();

  const lines = [];
  lines.push("# PropChain Work History (Start to Current)");
  lines.push("");
  lines.push("This file is auto-generated by `npm run session:end`.");
  lines.push("It is the single consolidated work record from project start to now.");
  lines.push("");
  lines.push("## Snapshot");
  lines.push("");
  lines.push(`- Generated UTC: ${generatedAt}`);
  lines.push(`- Branch: ${branch}`);
  lines.push(`- Total commits on branch: ${Number.isNaN(totalCommits) ? commits.length : totalCommits}`);
  if (firstCommit) {
    lines.push(
      `- Start point: ${firstCommit.shortHash} | ${firstCommit.authoredAtUtc} | ${firstCommit.author} | ${firstCommit.subject}`
    );
  }
  if (latestCommit) {
    lines.push(
      `- Current point: ${latestCommit.shortHash} | ${latestCommit.authoredAtUtc} | ${latestCommit.author} | ${latestCommit.subject}`
    );
  }
  lines.push(`- Documentation files indexed: ${docsFiles.length}`);
  lines.push("- Protected plan file: docs/PropChain_System_Plan.md (read-only, never modified by automation)");
  lines.push(`- Protected plan exists: ${fs.existsSync(protectedPlanPath) ? "yes" : "no"}`);
  lines.push(`- MAIN_HANDOVER session entries indexed: ${handoverEntries.length}`);
  lines.push(`- MASTER_CHANGELOG entries indexed: ${masterEntries.length}`);
  lines.push("");

  lines.push("## Documentation Inventory");
  lines.push("");
  for (const filePath of docsFiles) {
    lines.push(`- [${filePath}](${filePath})`);
  }
  lines.push("");

  lines.push("## Full Commit Timeline");
  lines.push("");
  lines.push("| # | UTC | Commit | Author | Files | Summary |");
  lines.push("|---|---|---|---|---:|---|");
  commits.forEach((commit, index) => {
    lines.push(
      `| ${index + 1} | ${escapeCell(commit.authoredAtUtc)} | ${commit.shortHash} | ${escapeCell(commit.author)} | ${commit.filesChanged} | ${escapeCell(commit.subject)} |`
    );
  });
  lines.push("");

  lines.push("## Session Log Mirror (from docs/MAIN_HANDOVER.md)");
  lines.push("");
  if (sessionLogMirror) {
    lines.push(sessionLogMirror);
  } else {
    lines.push("No Session Log section found in docs/MAIN_HANDOVER.md");
  }
  lines.push("");

  lines.push("## Master Changelog Mirror (from docs/MASTER_CHANGELOG.md)");
  lines.push("");
  if (masterEntriesMirror) {
    lines.push(masterEntriesMirror);
  } else {
    lines.push("No timestamped entries found in docs/MASTER_CHANGELOG.md");
  }
  lines.push("");

  lines.push("## End-of-Session Automation");
  lines.push("");
  lines.push("Run this command at the end of every session after completing code changes:");
  lines.push("");
  lines.push("```bash");
  lines.push("npm run session:end");
  lines.push("```");
  lines.push("");
  lines.push("Then stage docs updates together with code changes, commit, and push.");
  lines.push("Do not modify docs/PropChain_System_Plan.md during this process.");
  lines.push("");

  return lines.join("\n");
}

function main() {
  if (!fs.existsSync(docsDir)) {
    throw new Error("docs directory not found.");
  }

  const markdown = buildMarkdown();
  fs.writeFileSync(outputPath, markdown, "utf8");

  const relative = toPosix(path.relative(repoRoot, outputPath));
  console.log(`Updated ${relative}`);
}

main();
